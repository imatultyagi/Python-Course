# Q1. Shopping Bill Calculation


# Prices and quantities
notebooks = 3
notebook_price = 45

pens = 2
pen_price = 20

# Calculate total bill
total_bill = (notebooks * notebook_price) + (pens * pen_price)

# Amount given by customer
amount_given = 500

# Calculate remaining balance
remaining_balance = amount_given - total_bill

# Output
print("Total Bill Amount: (Rs)", total_bill)
print("Remaining Balance: (Rs)", remaining_balance)

